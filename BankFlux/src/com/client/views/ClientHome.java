    private void updateClientListViews() {
        
        JSONArray clients = ClientReader.getInstance().clientList();
        
        clientSituationsListPanel.removeAll();
        bankMovement.ClientSuggestionsPanel.removeAll();
        
        /**To update component views*/
        updateComponent();
        
        int count = clients.length();
        
        if(count > 0){
            
            bankMovement.tableModel.setData(clients.getJSONObject(0));

            for(int i = 0; i < count ; i++) {

                org.json.JSONObject client = clients.getJSONObject(i);

                clientSituationsListPanel.add(new ClientSituationsView(client));

                String nom = String.valueOf(i + 1) + " - " + client.getString("nom");

                CustomLabelView suggestionView = new CustomLabelView(nom);
                suggestionView.addMouseListener(new java.awt.event.MouseAdapter() {
                    @Override
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                       updateComponent();
                       bankMovement.tableModel.setData(client);
                    }
                });
                bankMovement.ClientSuggestionsPanel.add(suggestionView);
            }
        }
                      
    }
